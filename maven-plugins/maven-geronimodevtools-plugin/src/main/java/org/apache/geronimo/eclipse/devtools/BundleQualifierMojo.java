package org.apache.geronimo.eclipse.devtools;

import java.text.DateFormat;
import java.text.SimpleDateFormat;
import java.util.Date;

import org.apache.maven.plugin.AbstractMojo;
import org.apache.maven.plugin.MojoExecutionException;
import org.apache.maven.plugin.MojoFailureException;
import org.apache.maven.project.MavenProject;

/**
 * @goal qualifier
 *
 * @version $Rev$ $Date$
 */
public class BundleQualifierMojo extends AbstractMojo {
	
	/**
	 * @parameter expression="${project}"
	 */
	private MavenProject project;

	/* (non-Javadoc)
	 * @see org.apache.maven.plugin.Mojo#execute()
	 */
	public void execute() throws MojoExecutionException, MojoFailureException {
		DateFormat format = new SimpleDateFormat("yyyyMMddhhmm");
		String qualifier = "v" + format.format(new Date());
		getLog().info("Qualifier is: " + qualifier);
		project.getProperties().put("qualifier", qualifier);
	}

}
